/*
1. What is ADT ?
=> ADT refers to Abstraction Datatypes.
i. Datatype:
-> A data type is defined in 2 terms.
a. Data Representation, and
b. Operations on Data.
e.g. data types int, float, double, etc. are data representation and (+, -, /, %, ++, -- ,etc) are operations that can be perform on these data types.

ii. Abstraction:
-> The process of providing only the essentials and hiding the details is known as abstraction.
e.g. List ADT, Stack ADT, Queue ADT, etc.

1. List ADT :
    List -> 8, 3, 9, 10, 4, 6, 10, 11, 12.

    Operations:
        .add(el) / append(el);
        .add(indx, el) / insert (indx, el);
        .remove(indx);
        .set(indx, ele) / replace(indx, elem);
        .get(indx);
        .search(key) / contains(key);
        .sort();
        .reverse();
        .combine();

*/